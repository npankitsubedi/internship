CREATE EXTENSION IF NOT EXISTS pgcrypto;

DROP TABLE IF EXISTS approval_history;
DROP TABLE IF EXISTS applications;

CREATE TABLE applications (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    student_name VARCHAR(120) NOT NULL,
    registration_number VARCHAR(30) NOT NULL UNIQUE,
    student_email VARCHAR(150) NOT NULL,
    phone_number VARCHAR(20) NOT NULL,
    school VARCHAR(120) NOT NULL,
    program VARCHAR(120) NOT NULL,
    year_of_study INTEGER NOT NULL CHECK (year_of_study BETWEEN 1 AND 6),
    earned_credits INTEGER NOT NULL CHECK (earned_credits >= 0),
    internship_mode VARCHAR(20) NOT NULL CHECK (internship_mode IN ('CDC', 'NON_CDC')),
    company_name VARCHAR(160) NOT NULL,
    role_title VARCHAR(160) NOT NULL,
    stipend NUMERIC(10, 2),
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    location_scope VARCHAR(20) NOT NULL CHECK (location_scope IN ('INDIA', 'OUTSIDE_INDIA')),
    city VARCHAR(120) NOT NULL,
    state_name VARCHAR(120),
    global_region VARCHAR(120),
    country VARCHAR(120) NOT NULL,
    offer_letter_url TEXT NOT NULL,
    email_proof_url TEXT NOT NULL,
    placement_withdrawal_accepted BOOLEAN NOT NULL DEFAULT FALSE,
    placement_withdrawal_typed_name VARCHAR(120),
    status VARCHAR(30) NOT NULL,
    stage VARCHAR(30) NOT NULL,
    submitted_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    CHECK (end_date > start_date),
    CHECK (
        (internship_mode = 'CDC' AND placement_withdrawal_accepted = FALSE)
        OR (
            internship_mode = 'NON_CDC'
            AND placement_withdrawal_accepted = TRUE
            AND placement_withdrawal_typed_name IS NOT NULL
        )
    )
);

CREATE TABLE approval_history (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    application_id UUID NOT NULL REFERENCES applications(id) ON DELETE CASCADE,
    actor_role VARCHAR(30) NOT NULL,
    actor_name VARCHAR(120) NOT NULL,
    action VARCHAR(20) NOT NULL CHECK (action IN ('SUBMITTED', 'APPROVED', 'REJECTED')),
    comments TEXT,
    from_stage VARCHAR(30),
    to_stage VARCHAR(30),
    previous_status VARCHAR(30),
    new_status VARCHAR(30),
    action_at TIMESTAMP NOT NULL
);

CREATE INDEX idx_applications_stage ON applications(stage);
CREATE INDEX idx_applications_status ON applications(status);
CREATE INDEX idx_approval_history_application_id ON approval_history(application_id);

